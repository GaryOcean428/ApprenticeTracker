// This script is used to fix the lockfile errors during Vercel deployment
import fs from 'fs';

try {
  // Check if lockfile exists
  if (!fs.existsSync('yarn.lock')) {
    console.log('No yarn.lock found, creating an empty one');
    fs.writeFileSync('yarn.lock', '# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n');
  } else {
    console.log('yarn.lock exists, proceeding with build');
  }
} catch (err) {
  console.error('Error during lockfile check:', err);
  process.exit(1);
}
